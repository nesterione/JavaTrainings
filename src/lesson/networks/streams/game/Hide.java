package lesson.networks.streams.game;

import java.io.Serializable;

public class Hide implements Serializable{
    private int i;
    private int j;

    public Hide(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
